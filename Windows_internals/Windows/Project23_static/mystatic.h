#pragma once
class functions_math
{
public:
	static double add(double a,double b)
	{
		return a + b;
	}
	static double subtraction(double a, double b)
	{
		return a - b;
	}
	static double multiply(double a, double b)
	{
		return a * b;
	}
	static double divide(double a, double b)
	{
		return a / b;
	}
};
